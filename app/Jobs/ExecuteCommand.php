namespace Adshares\Adserver\Jobs;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Foundation\Bus\Dispatchable;
use Illuminate\Queue\InteractsWithQueue;
use Illuminate\Queue\SerializesModels;
use Illuminate\Support\Facades\Artisan;
use Illuminate\Support\Facades\Log;
use Symfony\Component\Console\Command\Command;

class ExecuteCommand implements ShouldQueue
{
    use Dispatchable;
    use InteractsWithQueue;
    use Queueable;
    use SerializesModels;

    public function __construct(private readonly string $signature)
    {
    }

    public function handle(): void
    {
        $exitCode = Artisan::call($this->signature);
        if (Command::SUCCESS !== $exitCode) {
            Log::error(sprintf('Job (command %s) failed', $this->signature));
        }
    }

    public function getSignature(): string
    {
        return $this->signature;
    }
}
